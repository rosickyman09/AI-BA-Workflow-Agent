class AddRagDocumentsRequest(BaseModel):
    doc_ids: List[str]


@router.get("/rag")
async def get_rag_documents(
    project_id: str = Query(...),
    current_user: TokenUser = Depends(require_readonly),
    db: Session = Depends(get_db),
):
    rows = db.execute(
        text(
            """
            SELECT doc_id::text AS doc_id, status
            FROM rag_documents
            WHERE project_id = :project_id
            ORDER BY added_at DESC
            """
        ),
        {"project_id": project_id},
    ).fetchall()

    return {
        "project_id": project_id,
        "doc_ids": [row.doc_id for row in rows],
        "items": [{"doc_id": row.doc_id, "status": row.status} for row in rows],
        "count": len(rows),
    }


@router.post("/rag")
async def add_rag_documents(
    payload: AddRagDocumentsRequest,
    current_user: TokenUser = Depends(require_ba),
    db: Session = Depends(get_db),
):
    doc_ids = [doc_id.strip() for doc_id in payload.doc_ids if isinstance(doc_id, str) and doc_id.strip()]
    if not doc_ids:
        raise HTTPException(status_code=400, detail="doc_ids must not be empty")

    now = datetime.utcnow()
    inserted_doc_ids: List[str] = []

    try:
        for doc_id in doc_ids:
            row = db.execute(
                text(
                    """
                    INSERT INTO rag_documents (id, doc_id, project_id, added_by, added_at, status)
                    SELECT gen_random_uuid(), d.doc_id, d.project_id, :added_by, :added_at, 'pending'
                    FROM documents d
                    WHERE d.doc_id = :doc_id
                      AND d.status = 'approved'
                    ON CONFLICT (doc_id) DO UPDATE SET added_at = rag_documents.added_at
                    RETURNING doc_id::text AS doc_id
                    """
                ),
                {
                    "doc_id": doc_id,
                    "added_by": current_user.user_id,
                    "added_at": now,
                },
            ).fetchone()

            if row and row.doc_id:
                inserted_doc_ids.append(row.doc_id)

        db.commit()
    except Exception:
        db.rollback()
        raise HTTPException(status_code=500, detail="Failed to add documents to RAG")

    return {
        "added_count": len(inserted_doc_ids),
        "doc_ids": inserted_doc_ids,
        "message": f"{len(inserted_doc_ids)} document(s) added to RAG",
    }


